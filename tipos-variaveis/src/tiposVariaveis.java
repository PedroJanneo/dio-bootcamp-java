public class tiposVariaveis {
    public static void main(String[] args) throws Exception {
       

        double salarioNinimo = 2500; // variavel ( muda de valor )
        System.out.println(salarioNinimo);

        final double minimo = 10; // o 'final' faz a variavel virar constante ( constante nao muda de valor)
        System.out.println(minimo); // esse metodo 'final' Ã© usado mto em valor que nao muda, ex: no PI
        System.out.println("Hello world");
    }
}
